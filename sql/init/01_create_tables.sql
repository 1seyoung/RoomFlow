-- RoomFlow Database Initialization Script

-- pgvector extension 활성화
CREATE EXTENSION IF NOT EXISTS vector;

-- 가구 제품 테이블
CREATE TABLE furniture_products (
    id SERIAL PRIMARY KEY,
    name VARCHAR(200) NOT NULL,
    category VARCHAR(50) NOT NULL,
    furniture_type VARCHAR(50) NOT NULL,
    price INTEGER,
    brand VARCHAR(100),
    image_url TEXT,
    
    -- 크기 정보 (배치용)
    width_cm INTEGER NOT NULL,
    height_cm INTEGER NOT NULL,
    depth_cm INTEGER NOT NULL,
    
    -- RAG/임베딩용
    description TEXT,
    features TEXT[],
    
    -- Vector 임베딩
    embedding VECTOR(1536),
    
    created_at TIMESTAMP DEFAULT NOW()
);

-- 인덱스
CREATE INDEX idx_category ON furniture_products(category);
CREATE INDEX idx_furniture_type ON furniture_products(furniture_type);
CREATE INDEX idx_embedding ON furniture_products USING ivfflat (embedding vector_cosine_ops);

